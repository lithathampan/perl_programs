#!/usr/bin/perl

$x=10;
$y=6;

$res=$x+$y;
print "The result of addition is $res\n";

$res=$x-$y;
print "The result of substraction is $res\n";

$res=$x*$y;
print "The result of multiplication is $res\n";

$res=$x/$y;
print "The result of division is $res\n";

$res=$x%$y;
print "The result of reminder operation is $res\n";

$res=$x**$y;
print "The result of power operation is $res\n";
