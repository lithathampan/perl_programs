#!/usr/bin/perl

$x="world";
$y="hello";

if ($x lt $y)
{
	print "$x is less than $y\n";
}
if ($x le $y)
{
	print "$x is less than or equal to $y\n";
}
if ($x gt $y)
{
	print "$x is greater than $y\n";
}
if ($x ge $y)
{
	print "$x is greater than or equal to $y\n";
}
if ($x eq $y)
{
	print "$x is equal to $y\n";
}
if ($x ne $y)
{
	print "$x is not equal to $y\n";
}
